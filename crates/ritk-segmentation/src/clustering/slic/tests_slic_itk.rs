fn to_f32(a: &[i32]) -> Vec<f32> {
    a.iter().map(|&v| v as f32).collect()
}

/// 12x12 image, three plateaus; g=4 (12/4=3, even), m=10, 10 iterations.
#[test]
fn matches_sitk_2d_three_plateaus() {
    let out = slic_itk_impl(&to_f32(&IMG2D), &[12, 12], &[4, 4], 10.0, 10);
    assert_eq!(out, to_f32(&G2D), "ITK-SLIC 2D core must match sitk label-for-label");
}

/// 6x9x9 volume, two blobs; g=3 (6/3,9/3 even), m=10, 5 iterations (3-D default).
#[test]
fn matches_sitk_3d_two_blobs() {
    let out = slic_itk_impl(&to_f32(&IMG3D), &[6, 9, 9], &[3, 3, 3], 10.0, 5);
    assert_eq!(out, to_f32(&G3D), "ITK-SLIC 3D core must match sitk label-for-label");
}
